def build_markdown():
    lines = ["# ДЗИ Математика — 250 Въпроса\n", "---\n"]
    cur = None
    for i, q in enumerate(QUESTIONS, 1):
        if q.subtopic != cur:
            cur = q.subtopic
            lines += [f"\n## {cur}\n"]
        lines += [f"### {i}. {q.question_html}  `[{q.difficulty}]`\n"]
        for lbl, txt in q.options.items():
            mark = " ✓" if lbl == q.correct else ""
            lines.append(f"- **{lbl})** {txt}{mark}")
        lines += [f"\n> {q.explanation}\n", "---\n"]
    return "\n".join(lines)


